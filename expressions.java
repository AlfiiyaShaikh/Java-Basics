
public class expressions {

	public static void main(String[] args) {
		/*
	    operator -- + , - , * ,.....
		operand -- values
		expressions -- operators & operands
		*/
		
		
		int friends =10;
		//friends=friends-1;  //alternative is
		friends--;   //same output
		
		double weight=10.8;
		//weight=weight-2;
		weight=(double) weight-2;   //alternative of above expression
		
		System.out.println(friends);
		System.out.println(weight);
	}

}
