
public class final_keyword {
	//used when the variable cannot change after declaring

	public static void main(String[] args) {
		
		
	   double pi1=3.12;          //this can be changed after 
	   pi1=2.1;  //like so
	   System.out.println(pi1);
	   
	   
	   final double PI = 3.14;      //this cannot change after , good prac to write in uppercase
       System.out.println(PI);
	}

}
